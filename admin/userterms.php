<div class="alert">
    <h2>Administrators</h2>    
      <ul>
        <li>Administrators are given with the highest previlege: manage the entire Portal</li>
        <li>Administrators are blocked from user view. If a user view is needed Log In as a user to view the page</li>
        <li>Administrators' previleges are extended to Moderating the portal</li>
      </ul>
    <h2>Moderators</h2>
      <ul>
        <li>Moderators are given with second level previlege: manage daily Portal data updates like attendance, marks etc.</li>
        <li>Moderators are also blocked from user view. If a user view is needed Log In as a user to view the page</li>
        <li>However, A moderator can view a users status through specific set of actions. View Guildelines at Moderators section</li>
        <li>Moderators cannot access or perform Administrative pages or actions</li>
      </ul>    
    <h2>Users</h2>
      <ul>
        <li>Users are previleged at third level in the Portal</li>
        <li>Users can only view the page results set by the Administrators and Moderators but cannot modify them</li>
      </ul>
</div>